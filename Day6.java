public class Day6 {
    public static void main(String[] args) {
        // Tipe data float
        float tinggiBadan = 164.5f;   // perlu huruf f di akhir
        float suhu = 30.6f;
        // Tipe data double
        double beratBadan = 55.223558780;  // lebih presisi
        double pi = 3.141592653589793;

        // Menampilkan nilai
        System.out.println("Contoh Tipe Data Pecahan:");
        // Tampilan float
        System.out.println("Tinggi Badan    : " + tinggiBadan);
        System.out.println("Suhu Tubuh      : " + suhu);
        // Tampilan double
        System.out.println("Berat Badan     : " + beratBadan);
        System.out.println("Nilai Pi        : " + pi);
    }
}
